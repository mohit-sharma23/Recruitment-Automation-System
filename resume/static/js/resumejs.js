
function call(){
    const p=document.getElementById('personal')
    const e=document.getElementById('educational')
    const s=document.getElementById('skills')
    const w=document.getElementById('work')
    p.classList.add('form-content-perosonal')
    p.classList.remove('form-content-perosonal-hide')
    e.classList.remove('form-content-educational')
    e.classList.add('form-content-educational-hide')
    s.classList.remove('form-content-skills')
    s.classList.add('form-content-skills-hide')
    w.classList.remove('form-content-work')
    w.classList.add('form-content-work-hide')
}

